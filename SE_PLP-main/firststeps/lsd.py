
# LIST
fruits = ["apple", "banana", "cherry"]

# Accessing elements
print(fruits[0])
 # Modifying an element
fruits[1] = "orange"
print(fruits[1])
print(fruits)

# Adding an element
fruits.append("grape")
print(fruits)

#TUPLES
coordinates = (10, 20)

 # Accessing elements
print(coordinates[0])  

# Trying to modify a tuple will raise an error:
 #coordinates[0] = 5  # Error! Tuples can't be modified.
print(coordinates[1])

#SETS
unique_numbers = {1, 2, 2, 3, 4}

print(unique_numbers)


#dictionaries
student_info = {"name": "Charlie", "age": 21, "grade": "A"}
 # Accessing a value by its key
print(student_info["name"])  
# Modifying a value
student_info["age"] = 22
print(student_info["age"])
student_info["name"] = "karanjaa"
print(student_info["name"])
print(student_info)